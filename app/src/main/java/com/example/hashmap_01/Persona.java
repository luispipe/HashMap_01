package com.example.hashmap_01;

public class Persona {

    String nombre;
    String fecha_nac;
    String telefono;
    String direccion;
    String ciudadNacientos;
    String estadoCivil;

    public Persona(String nombre, String fecha_nac, String telefono, String direccion, String ciudadNacientos, String estadoCivil) {
        this.nombre = nombre;
        this.fecha_nac = fecha_nac;
        this.telefono = telefono;
        this.direccion = direccion;
        this.ciudadNacientos = ciudadNacientos;
        this.estadoCivil = estadoCivil;
    }
}
